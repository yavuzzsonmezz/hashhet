package day01valuables;

public class Veriables03 {

    public static void main(String[] args) {
        int variable = 1;
        switch (variable){
            case 1 :
                System.out.print("P");
            case 2 :
            case 3 :
                System.out.print("Q");
            case 4 :
                System.out.print("R");
                break;
            default :
                System.out.print("S");
        }
    }
}
